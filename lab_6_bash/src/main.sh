#!/usr/bin/env bash

show_help() {
    echo "Использование: $0 -l КОЛИЧЕСТВО_СТРОК ВХОДНОЙ_ФАЙЛ ПРЕФИКС"
    echo
    echo "Скрипт разбивает входной файл на несколько файлов."
    echo "В каждом выходном файле будет не больше указанного количества строк."
    echo
    echo "Аргументы:"
    echo "  -l КОЛИЧЕСТВО_СТРОК   сколько строк записывать в один выходной файл"
    echo "  ВХОДНОЙ_ФАЙЛ          файл, который нужно разбить"
    echo "  ПРЕФИКС               начало имени выходных файлов"
    echo
    echo "Пример:"
    echo "  $0 -l 3 input.txt part_"
}

main() {
    if [[ "$1" == "-h" || "$1" == "--help" ]]; then
        show_help
        exit 0
    fi

    echo "Скрипт запущен"
}

main "$@"